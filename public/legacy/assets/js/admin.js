/**
 * Admin Dashboard JavaScript
 * Form enhancements and UI improvements
 */

(function() {
    'use strict';
    
    // Auto-generate slugs from names
    document.addEventListener('DOMContentLoaded', function() {
        const nameInputs = document.querySelectorAll('input[name="name"]');
        const slugInputs = document.querySelectorAll('input[name="slug"]');
        
        nameInputs.forEach((nameInput, index) => {
            const slugInput = slugInputs[index];
            if (nameInput && slugInput) {
                nameInput.addEventListener('input', function() {
                    if (!slugInput.value || slugInput.dataset.autoGenerated === 'true') {
                        slugInput.value = generateSlug(this.value);
                        slugInput.dataset.autoGenerated = 'true';
                    }
                });
                
                slugInput.addEventListener('input', function() {
                    this.dataset.autoGenerated = 'false';
                });
            }
        });
        
        // Image preview for file inputs - Enhanced for all image types
        const fileInputs = document.querySelectorAll('input[type="file"][accept*="image"]');
        fileInputs.forEach(input => {
            // Initialize preview for existing images
            const formGroup = input.closest('.form-group');
            if (formGroup) {
                const existingImg = formGroup.querySelector('img[src*="uploads"]');
                if (existingImg) {
                    // Mark existing image as preview
                    existingImg.classList.add('image-preview-live');
                    existingImg.style.marginTop = '10px';
                    existingImg.style.display = 'block';
                }
            }
            
            input.addEventListener('change', function(e) {
                const file = e.target.files[0];
                if (file && file.type.startsWith('image/')) {
                    const reader = new FileReader();
                    reader.onload = function(e) {
                        const formGroup = input.closest('.form-group');
                        if (!formGroup) return;
                        
                        // Find or create preview container
                        let preview = formGroup.querySelector('.image-preview-live, .image-preview, .logo-preview, .hero-preview');
                        
                        // Determine preview class based on input name
                        let previewClass = 'image-preview-live';
                        let maxWidth = '300px';
                        let maxHeight = '200px';
                        
                        if (input.name === 'logo') {
                            previewClass = 'logo-preview';
                            maxWidth = '200px';
                            maxHeight = '200px';
                        } else if (input.name === 'hero_image') {
                            previewClass = 'hero-preview';
                            maxWidth = '400px';
                            maxHeight = '250px';
                        } else if (input.name === 'image') {
                            // Could be menu item or category image
                            previewClass = 'image-preview-live';
                            maxWidth = '300px';
                            maxHeight = '200px';
                        }
                        
                        if (!preview) {
                            preview = document.createElement('img');
                            preview.className = previewClass;
                            preview.style.marginTop = '10px';
                            preview.style.maxWidth = maxWidth;
                            preview.style.maxHeight = maxHeight;
                            preview.style.borderRadius = '8px';
                            preview.style.border = '2px solid #e0e0e0';
                            preview.style.display = 'block';
                            preview.style.objectFit = 'cover';
                            
                            // Insert after the file input or after existing current image text
                            const currentImageDiv = formGroup.querySelector('div[style*="margin-top: 10px"]');
                            if (currentImageDiv) {
                                currentImageDiv.insertAdjacentElement('afterend', preview);
                            } else {
                                input.insertAdjacentElement('afterend', preview);
                            }
                        } else {
                            // Update existing preview
                            preview.className = previewClass;
                            preview.style.maxWidth = maxWidth;
                            preview.style.maxHeight = maxHeight;
                        }
                        
                        preview.src = e.target.result;
                        preview.style.display = 'block';
                        
                        // Hide "Current image" text if it exists
                        const currentImageText = formGroup.querySelector('p[style*="margin-bottom: 5px"]');
                        if (currentImageText && currentImageText.textContent.includes('Current')) {
                            currentImageText.style.display = 'none';
                        }
                        
                        // Hide old current image if it exists
                        const oldCurrentImg = formGroup.querySelector('img[src*="uploads"]:not(.image-preview-live)');
                        if (oldCurrentImg) {
                            oldCurrentImg.style.display = 'none';
                        }
                    };
                    reader.readAsDataURL(file);
                } else {
                    // Clear preview if invalid file
                    const formGroup = input.closest('.form-group');
                    if (formGroup) {
                        const preview = formGroup.querySelector('.image-preview-live, .image-preview, .logo-preview, .hero-preview');
                        if (preview && !preview.src.includes('uploads')) {
                            preview.remove();
                        }
                    }
                }
            });
        });
        
        // Form validation
        const forms = document.querySelectorAll('form');
        forms.forEach(form => {
            form.addEventListener('submit', function(e) {
                const requiredFields = form.querySelectorAll('[required]');
                let isValid = true;
                
                requiredFields.forEach(field => {
                    if (!field.value.trim()) {
                        isValid = false;
                        field.style.borderColor = '#dc3545';
                    } else {
                        field.style.borderColor = '';
                    }
                });
                
                if (!isValid) {
                    e.preventDefault();
                    alert('Please fill in all required fields.');
                }
            });
        });
    });
    
    function generateSlug(text) {
        return text.toLowerCase()
            .replace(/[^a-z0-9]+/g, '-')
            .replace(/^-+|-+$/g, '');
    }
})();

